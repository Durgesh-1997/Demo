package BasicsOfJava;

public class
incrementdecrement {
    public static void main(String[] args) {
        int x = 5;
//        System.out.println(x++);
//        System.out.println(x);
        x = x + 2;
        System.out.println(x);
        x++;
        System.out.println(x);
        ++x;
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);

    }
}
